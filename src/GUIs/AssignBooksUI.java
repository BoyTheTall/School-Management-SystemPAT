    private void btnAssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignActionPerformed
    boolean ForEntireGrade = radForEnireGrade.isSelected();
    
    if(ForEntireGrade == false){
    String Grade = txfGrade.getText();
    String Subject = txfSubject.getText();
   
    
    int GradeCode = Classes.UtilityClass.getGradeCode(Grade);
    Subject subject = new Subject(Subject);
    
    Book book = new Book(GradeCode, subject.getSubjectCode());
    
    txaStudentBookNumbers.append(book.assignBooks(Grade, Subject, ForEntireGrade));
    }
    
    if(ForEntireGrade == true){
    
    String Grade = txfGrade.getText();
    String Subject = txfSubject.getText();
   
    
    int GradeCode = Integer.parseInt(Grade);
    Subject subject = new Subject(Subject);
    
    Book book = new Book(GradeCode, subject.getSubjectCode());
    
    txaStudentBookNumbers.append(book.assignBooks(Grade, Subject, ForEntireGrade));
    }
    }//GEN-LAST:event_btnAssignActionPerformed

