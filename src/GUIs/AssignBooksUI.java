    private void btnGetQuantitesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGetQuantitesActionPerformed
boolean forEntireGrade;

forEntireGrade = radForEnireGrade.isSelected();
if(forEntireGrade == false){
int gradeCode = Classes.UtilityClass.getGradeCode(txfGrade.getText());//Getting Of Grade Code

Subject subject = new Subject(txfSubject.getText());
String SubjectCode = subject.getSubjectCode();

int noOfBooks= Book.getNoOfBooks(SubjectCode, gradeCode);


int noOfStudents = Book.getNoOfStudents(txfGrade.getText(), forEntireGrade);

txfNoOfStudents.setText(Integer.toString(noOfStudents));
txfNoOfBooks.setText(Integer.toString(noOfBooks));
}

if(forEntireGrade == true){
int gradeCode = Integer.parseInt(txfGrade.getText());//Getting Of Grade Code

Subject subject = new Subject(txfSubject.getText());
String SubjectCode = subject.getSubjectCode();

int noOfBooks= Book.getNoOfBooks(SubjectCode, gradeCode);


int noOfStudents = Book.getNoOfStudents(txfGrade.getText(), forEntireGrade);

txfNoOfStudents.setText(Integer.toString(noOfStudents));
txfNoOfBooks.setText(Integer.toString(noOfBooks));
}

    }//GEN-LAST:event_btnGetQuantitesActionPerformed

