    private void btnUpdateSubjectsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateSubjectsActionPerformed
        int noOfSubjects = 0;
              
        for(int i = 0;tblStudentSubjects.getValueAt(i, 0) != null; i++){
        noOfSubjects++;
        }
       
        @SuppressWarnings("MismatchedReadAndWriteOfArray")
       String [] subjects = new String[noOfSubjects];
       
        newSubjectCodes = new String[noOfSubjects];
       
       for(int z = 0; z < noOfSubjects ; z++){
     
       subjects[z] = (String) tblStudentSubjects.getValueAt(z, 0);
       
       }
       
       for(int a = 0; a < subjects.length; a++){
           String Subject = subjects[a];
           newSubjectCodes[a] = new Subject(Subject).getSubjectCode();
       }
       student.setSubjects(newSubjectCodes, oldSubjectCodes);
       
    }//GEN-LAST:event_btnUpdateSubjectsActionPerformed

