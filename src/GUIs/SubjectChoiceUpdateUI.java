    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
String StudentID;
StudentID = txfStudentCode.getText();

student = new Student(StudentID);

txfGrade.setText(student.getGrade());
txfStudentName.setText(student.getName() + " " +  student.getSurname());

ResultSet rs = student.getSubjects();

    try {
        rs.last();
       int no =  rs.getRow();
       //Reversing result set to first Row
       rs.first();
       //Reversing result set to before first row
       rs.previous();
       
        oldSubjectCodes = new String[no];
        for(int i = 0; rs.next(); i++){
            oldSubjectCodes[i] = rs.getString("SubjectCode");
           tblStudentSubjects.setValueAt(rs.getString("SubjectName"), i, 0);
           }
//Add the code to put in the Subjects in the jTable(Done in line 185)
    } catch (SQLException ex) {
        Logger.getLogger(SubjectChoiceUpdateUI.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_btnFindActionPerformed

